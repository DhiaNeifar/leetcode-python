def main(board):
    n = len(board)
    # Check entire row
    for i in range(n): 
        l = [0 for _ in range(9)]
        for j in range(n):
            if board[i][j] != '.':
                element = int(board[i][j])
                if l[element - 1]:
                    return False
                l[element - 1] = 1
    return True


